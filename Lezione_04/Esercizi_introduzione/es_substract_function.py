

#Funzione sottrazione a - b
def substract(a, b):
    result:int = a - b
    return result

#Esempio 8, 6 = 2
print(f"La differenza tra 8 e 6 è {substract(8, 6)}")


#Esempio 234, 211 = 23
print(f"La differenza tra 234 e 211 è {substract(234, 211)}")


#Esempio 7893, 342324 = -334431
print(f"La differenza tra 7893 e 342324 è {substract(7893, 342324)}")



